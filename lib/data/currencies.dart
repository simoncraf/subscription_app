String currencyFlag(String code) {
  final upper = code.toUpperCase();
  final override = _currencyFlagOverrides[upper];
  if (override != null) return override;
  if (_preciousMetalCodes.contains(upper)) return '🪙';
  if (upper.startsWith('X')) return '🌐';
  if (upper.length < 2) return '🌐';
  final country = upper.substring(0, 2);
  if (_countryCodes.contains(country)) {
    return _flagFromCountryCode(country);
  }
  return '🌐';
}

String _flagFromCountryCode(String countryCode) {
  final upper = countryCode.toUpperCase();
  if (upper.length != 2) return '🌐';
  final a = upper.codeUnitAt(0);
  final b = upper.codeUnitAt(1);
  if (a < 65 || a > 90 || b < 65 || b > 90) return '🌐';
  return String.fromCharCodes([
    0x1F1E6 + (a - 65),
    0x1F1E6 + (b - 65),
  ]);
}



